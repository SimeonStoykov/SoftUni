using System;

class FloatOrDouble
{
    static void Main()
    {
        double number1 = 34.567839023;
        float number2 = 12.345f;
        double number3 = 8923.1234857;
        float number4 = 3456.091f;
        Console.WriteLine("{0} {1} {2} {3}", number1, number2, number3, number4);
    }
}

